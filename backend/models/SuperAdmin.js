import mongoose from 'mongoose';

const superAdminSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const SuperAdmin = mongoose.model('super_admin', superAdminSchema);

export default SuperAdmin;
